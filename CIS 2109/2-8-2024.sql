-- Set Operations --
SELECT
    FIRST_NAME,
    LAST_NAME
FROM
    OT_EMPLOYEES     E
    INNER JOIN OT_CERTIFICATION C
    ON E.EMPLOYEE_ID = C.EMPLOYEE_ID
WHERE
    C.CETIFICATE_ID = 1
    OR C.CERTIFICATE_ID = 2;

-- Minus Union --
SELECT
    FIRST_NAME,
    LAST_NAME
FROM
    OT_EMPLOYEES     E
    INNER JOIN OT_CERTIFICATION C
    ON E.EMPLOYEE_ID = C.EMPLOYEE_ID
WHERE
    C.CETIFICATE_ID = 1 MINUS
    SELECT
        FIRST_NAME,
        LAST_NAME
    FROM
        OT_EMPLOYEES     E
        INNER JOIN OT_CERTIFICATION C
        ON E.EMPLOYEE_ID = C.EMPLOYEE_ID
    WHERE
        C.CERTIFICATE_ID = 2;

-- Union --SELECT
SELECT
    FIRST_NAME,
    LAST_NAME
FROM
    OT_EMPLOYEES     E
    INNER JOIN OT_CERTIFICATION C
    ON E.EMPLOYEE_ID = C.EMPLOYEE_ID
WHERE
    C.CETIFICATE_ID = 1
UNION
SELECT
    FIRST_NAME,
    LAST_NAME
FROM
    OT_EMPLOYEES     E
    INNER JOIN OT_CERTIFICATION C
    ON E.EMPLOYEE_ID = C.EMPLOYEE_ID
WHERE
    C.CERTIFICATE_ID = 2;