    ITEM_NAME
FROM
    ITEMS  I
WHERE
    ARTIST_ID IN(
        SELECT
            ARTIST_ID
        FROM
            ARTIST A
        WHERE
            A.ARTIST_ID = I.ARTIST_ID
            AND ARTIST_NAME < 'Cat'
    );

