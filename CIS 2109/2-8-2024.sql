    ITEM_NAME
FROM
    ITEMS  I
WHERE
    EXISTS(
        SELECT
            1
        FROM
            ARTIST A
        WHERE
            A.ARTIST_ID = I.ARTIST_ID
            AND ARTIST_NAME < 'Cat'
    );

