-- Question 4 --
SELECT
    FIRST_NAME,
    LAST_NAME,
    SALARY,
    ROUND(SALARY * 03.8398, 2) AS ROUNDED_CITY_TAX
FROM
    HR_EMPLOYEES
ORDER BY
    ROUNDED_CITY_TAX DESC;

