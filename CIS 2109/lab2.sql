SELECT
    HR_EMPLOYEES.FIRST_NAME,
    HR_EMPLOYEES.LAST_NAME,
    HR_EMPLOYEES.HIRE_DATE,
    NVL(HR_EMPLOYEES.COMMISSION_PCT, 0)
